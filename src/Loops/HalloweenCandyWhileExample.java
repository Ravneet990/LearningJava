package Loops;

public class HalloweenCandyWhileExample {

	int noOfChocolates;

	void trickOrTreating() {
	
		do {
		
			
			System.out.println("Trick or Treat");
			
			noOfChocolates++;
			
			
			
			System.out.println("Go to the next house");
			
			System.out.println("Number of chocolate :" + noOfChocolates);
			
			
			
			
		
		}
		
		while (noOfChocolates<10);
	
	}

}
